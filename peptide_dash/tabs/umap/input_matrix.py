def _umap_source_mode(value) -> str:
    """Normalize UMAP input-source preset."""
    v = str(value or UMAP_SOURCE_BASIC).strip().lower()
    aliases = {
        "native": UMAP_SOURCE_BASIC,
        "basic_native": UMAP_SOURCE_BASIC,
        "feature": UMAP_SOURCE_BASIC,
        "features": UMAP_SOURCE_BASIC,
        "basin": UMAP_SOURCE_THERMO,
        "basin_geometry": UMAP_SOURCE_THERMO,
        "corrected": UMAP_SOURCE_THERMO,
        "thermo": UMAP_SOURCE_THERMO,
        "pmf": UMAP_SOURCE_PMF,
        "pmfs": UMAP_SOURCE_PMF,
        "shape": UMAP_SOURCE_PMF,
        "pmf_vector": UMAP_SOURCE_PMF,
        "pmf_vectors": UMAP_SOURCE_PMF,
        "seq": UMAP_SOURCE_SEQUENCE,
        "sequence": UMAP_SOURCE_SEQUENCE,
        "letters": UMAP_SOURCE_SEQUENCE,
        "letter": UMAP_SOURCE_SEQUENCE,
        "aminoacid": UMAP_SOURCE_SEQUENCE,
        "amino_acid": UMAP_SOURCE_SEQUENCE,
        "aa": UMAP_SOURCE_SEQUENCE,
    }
    valid = {UMAP_SOURCE_BASIC, UMAP_SOURCE_THERMO, UMAP_SOURCE_PMF, UMAP_SOURCE_SEQUENCE}
    return aliases.get(v, v if v in valid else UMAP_SOURCE_BASIC)


def _is_sequence_feature_col(col: str) -> bool:
    s = str(col)
    return (
        s.startswith("seq_")
        or s == "sequence"
        or s in {"L", "KD_mean", "KD_std", "KD_min", "KD_max", "KD_Nterm", "KD_Cterm"}
    )


def _sequence_umap_feature_cols(df: pd.DataFrame, raw_cols: Optional[Sequence[str]] = None) -> List[str]:
    """Columns for sequence/letter-space UMAP input.

    Prefer normalized, interpretable descriptors generated by batch_ana. Raw
    residue counts are intentionally excluded from the embedding by default
    because they mostly duplicate sequence length; counts remain available for
    ML outside this tab.
    """
    if not isinstance(df, pd.DataFrame) or df.empty:
        return []
    raw = list(raw_cols) if raw_cols is not None else [c for c in df.columns if pd.api.types.is_numeric_dtype(df[c])]
    raw_set = {str(c) for c in raw}
    aa_order = "ACDEFGHIKLMNPQRSTVWY"
    preferred: List[str] = []

    explicit = [
        "seq_length",
        "seq_known_frac",
        "seq_unknown_frac",
        "seq_net_charge_pH7_approx",
        "seq_abs_net_charge_pH7_approx",
        "seq_charge_density_pH7_approx",
        "seq_abs_charge_density_pH7_approx",
        "seq_kd_mean",
        "seq_kd_std",
        "seq_kd_min",
        "seq_kd_max",
        "seq_kd_range",
        "seq_kd_nterm",
        "seq_kd_cterm",
        "seq_composition_entropy_bits",
        "seq_composition_entropy_norm20",
        "seq_unique_aa_count",
        "seq_unique_aa_frac20",
        "seq_max_run_len",
        "seq_max_run_frac",
    ]
    preferred.extend([c for c in explicit if c in raw_set])
    preferred.extend([f"seq_aa_{aa}_frac" for aa in aa_order if f"seq_aa_{aa}_frac" in raw_set])
    for prefix in ("seq_frac_", "seq_nterm_", "seq_cterm_"):
        preferred.extend(sorted([c for c in raw_set if c.startswith(prefix)]))

    # Backward-compatible minimal sequence columns from older batch_ana outputs.
    if not preferred:
        fallback = ["L", "KD_mean", "KD_std", "KD_min", "KD_max", "KD_Nterm", "KD_Cterm"]
        preferred.extend([c for c in fallback if c in raw_set])

    # Deduplicate while preserving order.
    out: List[str] = []
    seen = set()
    for c in preferred:
        if c in seen or c not in df.columns:
            continue
        if not pd.api.types.is_numeric_dtype(df[c]):
            continue
        out.append(c)
        seen.add(c)
    return out


def _wide_feature_stat(col: object) -> Optional[str]:
    c = str(col)
    if "__" not in c:
        return None
    return c.split("__", 1)[1]


def _is_pmf_core_wide_col(col: object) -> bool:
    return _wide_feature_stat(col) in set(PMF_CORE_FEATURES)


def _is_pmf_physical_annotation_wide_col(col: object) -> bool:
    return _wide_feature_stat(col) in set(PMF_PHYSICAL_ANNOTATIONS)


def _pmf_core_wide_cols(df: pd.DataFrame, cols: Optional[List[str]] = None) -> List[str]:
    if not isinstance(df, pd.DataFrame) or df.empty:
        return []
    base_cols = cols if cols is not None else list(df.columns)
    return [
        c for c in base_cols
        if c in df.columns
        and c != "variant"
        and pd.api.types.is_numeric_dtype(df[c])
        and _is_pmf_core_wide_col(c)
    ]


def _transform_wide_pmf_core(df_num: pd.DataFrame) -> Tuple[pd.DataFrame, str]:
    """Apply PMF intrinsic transforms to wide metric__feature columns."""
    if not isinstance(df_num, pd.DataFrame) or df_num.empty:
        return pd.DataFrame(index=getattr(df_num, "index", None)), ""
    tmp = df_num.copy()
    rename: Dict[str, str] = {}
    rev: Dict[str, str] = {}
    rules: Dict[str, str] = {}
    for i, c in enumerate(tmp.columns.astype(str)):
        stat = _wide_feature_stat(c) or c
        tmp_name = f"{stat}__wide{i}"
        rename[c] = tmp_name
        rev[tmp_name] = c
        rules[tmp_name] = PMF_TRANSFORM_RULES.get(stat, "zscore")
    tmp = tmp.rename(columns=rename)
    Xt, meta = transform_features(tmp, rules, scaler_scope="current_selection")
    Xt = Xt.rename(columns=rev)
    used = sorted({str(v) for v in meta.get("columns", {}).values() if v != "none"})
    note = "thermodynamic PMF-core transforms applied"
    if used:
        note += f" ({', '.join(used)})"
    return Xt, note


